document.getElementById("burger-menu").addEventListener("click", function () {
  const navLinks = document.getElementById("nav-links");
  navLinks.classList.toggle("show");
});

const contactForm = document.getElementById("contactForm");
if (contactForm) {
  contactForm.addEventListener("submit", function (event) {
    event.preventDefault();

    const name = document.getElementById("name").value.trim();
    const email = document.getElementById("email").value.trim();
    const message = document.getElementById("message").value.trim();

    if (name !== "" && email !== "" && message !== "") {
      document.getElementById("thankYouMessage").style.display = "block";

      contactForm.reset();
    }
  });
}

// Switch language function
function changeLanguage(language) {
  const elements = document.querySelectorAll("[data-translate-key]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-translate-key");
    if (translations[language] && translations[language][key]) {
      if (element.tagName === "IMG") {
        element.setAttribute("alt", translations[language][key]);
      } else if (
        element.tagName === "INPUT" ||
        element.tagName === "TEXTAREA"
      ) {
        element.setAttribute("placeholder", translations[language][key]);
      } else if (element.tagName === "OPTION") {
        element.textContent = translations[language][key];
      } else {
        element.textContent = translations[language][key];
      }
    } else {
      console.warn(`Missing translation for ${key} in ${language}`);
    }
  });
}

// Event listener language switch
document
  .getElementById("language-switcher")
  .addEventListener("change", (event) => {
    const selectedLanguage = event.target.value;
    changeLanguage(selectedLanguage);
    localStorage.setItem("selectedLanguage", selectedLanguage);
  });

// Start language -> Italian
window.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem("selectedLanguage") || "it";
  changeLanguage(savedLanguage);
  document.getElementById("language-switcher").value = savedLanguage;
});
